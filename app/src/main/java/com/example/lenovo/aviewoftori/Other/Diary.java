public class Diary {

    private String content;

    private String time;

    private String image;

    public Diary(String content, String time, String image) {

        this.content = content;

        this.time = time;

        this.image = image;

    }

    public String getContent() {

        return content;

    }

    public String getTime() {

        return time;

    }

    public String getImage() {

        return image;

    }
}
